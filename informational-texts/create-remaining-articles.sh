# Medical Ethics: Playing God

When Victor Frankenstein creates his creature, he crosses a line that still defines debates about medical ethics today. What are doctors and scientists allowed to do? Where should we draw boundaries? These questions matter more now than ever as technology gives us powers Mary Shelley could barely imagine.

## The Hippocratic Oath

For over 2,000 years, the Hippocratic Oath has guided medical ethics. Ancient Greek physician Hippocrates established principles including "First, do no harm" and maintaining patient confidentiality. Doctors promise to use their skills to help people, not hurt them.

But what counts as "harm"? If an experimental treatment might cure someone but could also kill them, is trying it harmful or helpful? These dilemmas have no easy answers.

## Consent and Autonomy

Modern medical ethics requires **informed consent**—patients must understand risks and agree to treatment. Forcing treatment on unwilling people, even to help them, violates their **autonomy** (right to make their own decisions).

Victor Frankenstein never gets his creature's consent to exist. The creature didn't ask to be created, didn't choose his appearance, and wasn't informed about the consequences. Victor makes all decisions alone, treating his creation as an experiment rather than a person with rights.

This mirrors real ethical violations in medical history. The Tuskegee Syphilis Study (1932-1972) denied treatment to Black men without their informed consent. Nazi doctors conducted horrific experiments on concentration camp prisoners. These atrocities led to strict ethical rules protecting research subjects.

## The Duty of Care

Doctors have ongoing responsibility for patients—they can't abandon them when treatment gets difficult. Victor violates this principle catastrophically. The moment his creature lives, Victor runs away in horror. He created life but accepts no responsibility for it.

Imagine a doctor who performs surgery, then flees the hospital leaving the patient to recover alone. This is essentially what Victor does. His creature needs guidance, education, and support. Victor provides none of these things.

## Playing God

Critics accuse scientists of "playing God" when they manipulate life. But what does this mean? Where's the line between acceptable medical intervention and forbidden interference with nature?

We accept some interventions: vaccines prevent diseases, surgery repairs bodies, medicine extends life. We're less comfortable with others: genetic engineering, human cloning, artificial life. The difference isn't always clear.

Victor Frankenstein pursued knowledge without considering ethics. He asked "Can I create life?" but not "Should I?" or "What happens after?" This remains relevant as modern science tackles issues like:

- **Gene editing**: Fixing genetic diseases vs. designing "perfect" babies  
- **Life extension**: Adding years to life vs. achieving immortality  
- **Artificial intelligence**: Creating intelligence vs. creating consciousness  
- **Cloning**: Copying cells vs. copying people

## The Responsibility of Scientists

Scientists aren't just responsible for their discoveries—they're responsible for how those discoveries get used. Nuclear physics can power cities or destroy them. Genetic engineering can cure diseases or create biological weapons. Knowledge itself is neutral; its application carries moral weight.

Mary Shelley argued through *Frankenstein* that scientific progress without ethical consideration leads to tragedy. Victor has the knowledge to create life but lacks the wisdom to handle what he creates. His creature becomes a killer not because it's inherently evil but because Victor abandoned it.

## Modern Medical Ethics Principles

Today's medical ethics rest on four pillars:

**Autonomy**: Respecting patients' rights to make their own decisions  
**Beneficence**: Acting to benefit patients  
**Non-maleficence**: Avoiding harm  
**Justice**: Treating people fairly and distributing resources equitably

Victor violates all four: he doesn't respect his creature's autonomy, doesn't act for its benefit, causes tremendous harm, and treats it unjustly.

## Questions to Consider

*Frankenstein* raises questions still debated:

- Do created beings have rights? (relevant for AI, clones, modified organisms)  
- Is creating life without ability to care for it ethical?  
- Should all knowledge be pursued, regardless of consequences?  
- Who bears responsibility when scientific creations harm people?

These aren't just theoretical questions. As we develop more powerful technologies, we must answer them. Otherwise, like Victor Frankenstein, we might create things we can't control and refuse to take responsibility for.

## The Lesson

Medical and scientific ethics aren't about limiting knowledge—they're about using knowledge responsibly. Victor Frankenstein represents the danger of brilliant minds without moral grounding. His story reminds us that creating life (or anything powerful) carries obligations we can't abandon when things get difficult.

Progress requires both capability and wisdom. We must ask not just "Can we?" but "Should we?" and "What then?" Otherwise, we risk becoming like Victor: creators who regret what we've created but refuse to clean up the mess.

